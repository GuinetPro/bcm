    {
        $visita  = new Visita;

        $cliente = Cliente::find((int)$request->input('cliente_id'));

        if (!$cliente) {
             \Flash::error('El Cliente que busca no existe.');
            return redirect()->back();
        }
      


        return view("visitas.create",["visita" => $visita,'cliente'=> $cliente]);
    }

